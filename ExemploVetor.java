public class ExemploVetor {
    public static void main(String[] args) {
        // Declara e inicializa o vetor com 5 posições
        int[] numeros = new int[5];

        // Atribui valores às posições
        numeros[0] = 10;
        numeros[1] = 20;
        numeros[2] = 30; // Terceira posição (índice 2)
        numeros[3] = 40;
        numeros[4] = 50;

        // Imprime o valor da terceira posição
        System.out.println("O valor na terceira posição é: " + numeros[2]);
    }
}
